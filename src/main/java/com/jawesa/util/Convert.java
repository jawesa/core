package com.jawesa.util;

public class Convert {
	public final static String intToString(int value) {
		return String.valueOf(value);
	}
	
	public final static String longToString(long value) {
		return String.valueOf(value);
	}
	
	public final static String floatToString(float value) {
		return String.valueOf(value);
	}
	
	public final static String doubleToString(double value) {
		return String.valueOf(value);
	}
}
